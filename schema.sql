values ('event-images', 'event-images', true)
on conflict (id) do nothing;

-- Policies for storage bucket (using do block to avoid "already exists" errors)
do $$
begin
  -- Allow public read access to images
  if not exists (select 1 from pg_policies where policyname = 'Public Access' and tablename = 'objects' and schemaname = 'storage') then
    create policy "Public Access" on storage.objects for select using ( bucket_id = 'event-images' );
  end if;
  
  -- Allow anyone to upload images (In a real app, restrict this to admin)
  if not exists (select 1 from pg_policies where policyname = 'Public Upload' and tablename = 'objects' and schemaname = 'storage') then
    create policy "Public Upload" on storage.objects for insert with check ( bucket_id = 'event-images' );
  end if;
  
  -- Allow anyone to delete images (In a real app, restrict this to admin)
  if not exists (select 1 from pg_policies where policyname = 'Public Delete' and tablename = 'objects' and schemaname = 'storage') then
    create policy "Public Delete" on storage.objects for delete using ( bucket_id = 'event-images' );
  end if;
end
$$;

