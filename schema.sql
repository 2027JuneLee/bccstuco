-- Create the questions table if it doesn't exist
create table if not exists public.questions (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  question text not null,
  answer text,
  author_name text default 'Anonymous',
  is_answered boolean default false
);

-- Enable Row Level Security (RLS)
alter table public.questions enable row level security;

-- Create policies (using do block to avoid "already exists" errors)
do $$
begin
  if not exists (select 1 from pg_policies where policyname = 'Enable read access for all users' and tablename = 'questions') then
    create policy "Enable read access for all users" on public.questions for select using (true);
  end if;
  if not exists (select 1 from pg_policies where policyname = 'Enable insert access for all users' and tablename = 'questions') then
    create policy "Enable insert access for all users" on public.questions for insert with check (true);
  end if;
  if not exists (select 1 from pg_policies where policyname = 'Enable update access for all users' and tablename = 'questions') then
    create policy "Enable update access for all users" on public.questions for update using (true);
  end if;
  if not exists (select 1 from pg_policies where policyname = 'Enable delete access for all users' and tablename = 'questions') then
    create policy "Enable delete access for all users" on public.questions for delete using (true);
  end if;
end
$$;

-- Create the events table if it doesn't exist
create table if not exists public.events (
  id bigint generated by default as identity primary key,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  title text not null,
  date date not null,
  body text,
  image_url text,
  is_upcoming boolean default true,
  category text default 'general' -- 'general' (STUCO) or 'club'
);

-- Add is_upcoming column if it doesn't exist (in case table existed but was old)
do $$
begin
  if not exists (select 1 from information_schema.columns where table_name='events' and column_name='is_upcoming') then
    alter table public.events add column is_upcoming boolean default true;
  end if;
end
$$;

-- Add category column if it doesn't exist
do $$
begin
  if not exists (select 1 from information_schema.columns where table_name='events' and column_name='category') then
    alter table public.events add column category text default 'general';
  end if;
end
$$;


